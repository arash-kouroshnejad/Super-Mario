package core.objects;

public class Bounds {
    public final int TOP;
    public final int BOTTOM;
    public final int RIGHT;
    public final int LEFT;

    public Bounds(int TOP, int BOTTOM, int RIGHT, int LEFT) {
        this.TOP = TOP;
        this.BOTTOM = BOTTOM;
        this.RIGHT = RIGHT;
        this.LEFT = LEFT;
    }
}
